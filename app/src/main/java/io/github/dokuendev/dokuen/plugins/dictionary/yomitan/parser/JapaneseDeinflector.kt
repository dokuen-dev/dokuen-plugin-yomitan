    /**
     * Recursively deinflects a Japanese word to yield all possible base dictionary form candidates.
     * Memory-safe BFS search tracks exact inflection chains.
     */
    fun deinflect(word: String): List<DeinflectionResult> {
        val results = mutableListOf<DeinflectionResult>()
        results.add(DeinflectionResult(word, emptyList()))

        val queue = mutableListOf<DeinflectionResult>()
        queue.add(DeinflectionResult(word, emptyList()))

        var head = 0
        while (queue.size in (head + 1)..<250) {
            val current = queue[head++]

            for (rule in rules) {
                if (current.term.endsWith(rule.suffix)) {
                    val base = current.term.substring(0, current.term.length - rule.suffix.length) + rule.replacement
                    if (base.isNotEmpty() && base != current.term) {
                        val nextRules = current.rules + rule.name
                        val newResult = DeinflectionResult(base, nextRules)
                        if (results.none { it.term == base }) {
                            results.add(newResult)
                            queue.add(newResult)
                        }
                    }
                }
            }
        }
        return results
    }
